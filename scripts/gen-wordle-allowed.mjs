// Generates src/games/wordle/allowed.ts — the large set of valid 5-letter
// guesses used to validate Wordle input. Source: the SCOWL-based `word-list`
// npm package (a devDependency). The daily ANSWER pool stays in words.ts; this
// list only widens what players are allowed to *guess*.
//
//   Run with:  npm run gen:wordle
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import wordListPath from 'word-list'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '..', 'src', 'games', 'wordle', 'allowed.ts')

const words = fs
  .readFileSync(wordListPath, 'utf8')
  .split('\n')
  .map((w) => w.trim().toLowerCase())
  .filter((w) => /^[a-z]{5}$/.test(w))

const unique = Array.from(new Set(words)).sort()

// 12 words per line keeps the generated file diff-friendly.
const lines = []
for (let i = 0; i < unique.length; i += 12) {
  lines.push('  ' + unique.slice(i, i + 12).map((w) => `'${w}'`).join(', ') + ',')
}

const out = `// AUTO-GENERATED by scripts/gen-wordle-allowed.mjs — do not edit by hand.
// ${unique.length} valid 5-letter guesses (SCOWL-based 'word-list' package).
// Used ONLY to validate guesses; the daily answer pool is the curated list in
// words.ts. Regenerate with: npm run gen:wordle
/* eslint-disable */
export const ALLOWED_GUESSES: string[] = [
${lines.join('\n')}
]

/** Fast membership set for guess validation. */
export const ALLOWED_SET: ReadonlySet<string> = new Set(ALLOWED_GUESSES)
`

fs.writeFileSync(OUT, out)
console.log(`Wrote ${unique.length} words to ${path.relative(process.cwd(), OUT)}`)
